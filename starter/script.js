const formatMovement = function (date, local) {

  const calcDates = function (date1, date2) {
    return Math.round(Math.abs(date2 - date1) / (1000 * 60 * 60 * 24));
  }

  const passDates = calcDates(new Date(), date); //이걸로 현재인지 아닌지를 비교 -> 계산하는 함수에 인자를 넣어주면서 실행, 그리고 그 값을 저장해 사용

  return passDates === 0 ? 'Today' : passDates === -1 ? 'Yesterday' : passDates <= 7 ? '1 week ago' : `${new Intl.DateTimeFormat(local).format(date)}`

}

const formatCurrency = function(value, acc) {

  return new Intl.NumberFormat(acc.locale, {
    style: 'currency',
    currency: acc.currency
  }).format(value);
}

